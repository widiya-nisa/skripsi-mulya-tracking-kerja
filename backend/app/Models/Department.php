<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Department extends Model
{
    protected $fillable = [
        'name',
        'description',
        'type',
    ];

    /**
     * Get users in this department
     */
    public function users()
    {
        return $this->hasMany(User::class, 'job_description', 'name');
    }
}
